public class e_IncrementDecrement {
    public static void main(String[] args) {
    int x =5;
    System.out.println(x);

    x++; // Increment x=x+1
    System.out.println(x);

    x--; // Decrement x=x-1
    System.out.println(x);

    // Pre Increment and Decrement
        System.out.println(++x);  // Increment than Use x
        System.out.println(--x);  // Decrement than Use x

    // Post Inctrment and Decrement
        System.out.println(x++); // Use x than Increment
        System.out.println(x--); // Use x than Decrement

    }
}