public class f_DoubleDatatype {
    public static void main(String[] args) {
        double x=5;
        double y=2;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x/y);

        double z = 5/2; // Integer and Integer operation are Integer
        System.out.println(z);

    }
}
